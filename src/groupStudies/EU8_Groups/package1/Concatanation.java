package groupStudies.EU8_Groups.package1;

public class Concatanation {
    public static void main(String[] args) {
        //Concatenation and addition exercise

        System.out.println (1 + 25 + 2 + "aws");
        System.out.println(1 + "aws" + (2 + 4));
        System.out.println("aws" + 1 + 2 + 3);
        System.out.println('a' + 'w' + 's');
        System.out.println('w' + " " + 's');
        System.out.println('a' + "" + 's');
        System.out.println('w' + 's');
        System.out.println('w');
    }
}
