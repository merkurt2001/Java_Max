package weeklyMeeting.GroupStudies.SwitchStatement;

public class switchExample7 {

    public static void main(String[] args) {

        int year = 2004;

        switch (year) {

            //case 2001: case 2002: case 2003:case 2004:
            case 2005:
                System.out.print("Normal Year");
                break;
            case 2004:
                System.out.print("Leap Year");
                break;
        }


    }
}
