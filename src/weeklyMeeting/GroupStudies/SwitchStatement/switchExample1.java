package weeklyMeeting.GroupStudies.SwitchStatement;

public class switchExample1 {

    public static void main(String[] args) {
        int i = 1;
        switch (i) {

            default:
                System.out.println("Default ");
            case 1:
                System.out.println("1 ");
                break;
            case 0:
                System.out.println("0 ");

        }//1
    }
}
